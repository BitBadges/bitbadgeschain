package helpers

import (
	"fmt"
	"os"
	"path/filepath"
	"strings"

	"github.com/ethereum/go-ethereum/accounts/abi"
)

var (
	// CompiledBytecodePath is where solcjs outputs the bytecode file for GammHelperLibrariesTestContract
	CompiledBytecodePath = "contracts/test/test_GammHelperLibrariesTestContract_sol_GammHelperLibrariesTestContract.bin"
	// CompiledABIPath is where solcjs outputs the ABI file for GammHelperLibrariesTestContract
	CompiledABIPath = "contracts/test/test_GammHelperLibrariesTestContract_sol_GammHelperLibrariesTestContract.abi"
)

// GetGammContractABI returns the ABI for the GammHelperLibrariesTestContract
// Loads from compiled ABI file (generated by `make compile-contracts`)
func GetGammContractABI() (abi.ABI, error) {
	abiPath := CompiledABIPath
	repoRoot := findRepoRoot()

	// Try multiple possible locations
	possiblePaths := []string{
		filepath.Join(repoRoot, abiPath), // From repo root (most reliable)
		abiPath,                          // Direct path (if running from repo root)
		filepath.Join("..", "..", "..", "..", "..", "..", abiPath), // From x/gamm/precompile/test/integration
		filepath.Join("..", "..", "..", "..", abiPath),             // From x/gamm/precompile
		filepath.Join(".", abiPath),
	}

	var abiBytes []byte
	var readErr error
	for _, path := range possiblePaths {
		abiBytes, readErr = os.ReadFile(path)
		if readErr == nil {
			break
		}
	}

	if readErr != nil {
		return abi.ABI{}, fmt.Errorf("failed to read contract ABI from %s (tried: %v). Run 'make compile-contracts' first: %w", abiPath, possiblePaths, readErr)
	}

	contractABI, parseErr := abi.JSON(strings.NewReader(string(abiBytes)))
	if parseErr != nil {
		return abi.ABI{}, fmt.Errorf("failed to parse ABI: %w", parseErr)
	}

	return contractABI, nil
}

// GetGammContractBytecode returns the bytecode for the GammHelperLibrariesTestContract
// Loads from compiled bytecode file (generated by `make compile-contracts`)
func GetGammContractBytecode() ([]byte, error) {
	bytecodePath := CompiledBytecodePath
	repoRoot := findRepoRoot()

	// Try multiple possible locations
	possiblePaths := []string{
		filepath.Join(repoRoot, bytecodePath), // From repo root (most reliable)
		bytecodePath,                          // Direct path (if running from repo root)
		filepath.Join("..", "..", "..", "..", "..", "..", bytecodePath), // From x/gamm/precompile/test/integration
		filepath.Join("..", "..", "..", "..", bytecodePath),             // From x/gamm/precompile
		filepath.Join(".", bytecodePath),
	}

	var bytecodeHexBytes []byte
	var readErr error
	for _, path := range possiblePaths {
		bytecodeHexBytes, readErr = os.ReadFile(path)
		if readErr == nil {
			break
		}
	}

	if readErr != nil {
		return nil, fmt.Errorf("failed to read contract bytecode from %s (tried: %v). Run 'make compile-contracts' first: %w", bytecodePath, possiblePaths, readErr)
	}

	// Remove newlines and trim
	bytecodeHex := strings.TrimSpace(string(bytecodeHexBytes))
	bytecodeHex = strings.ReplaceAll(bytecodeHex, "\n", "")
	bytecodeHex = strings.TrimPrefix(bytecodeHex, "0x")

	// Convert hex string to bytes
	bytecode := make([]byte, len(bytecodeHex)/2)
	for i := 0; i < len(bytecodeHex); i += 2 {
		var b byte
		_, scanErr := fmt.Sscanf(bytecodeHex[i:i+2], "%02x", &b)
		if scanErr != nil {
			return nil, fmt.Errorf("invalid hex string in bytecode: %w", scanErr)
		}
		bytecode[i/2] = b
	}

	return bytecode, nil
}

// findRepoRoot finds the repository root by looking for go.mod
func findRepoRoot() string {
	dir, err := os.Getwd()
	if err != nil {
		return "."
	}

	for {
		if _, err := os.Stat(filepath.Join(dir, "go.mod")); err == nil {
			return dir
		}
		parent := filepath.Dir(dir)
		if parent == dir {
			break
		}
		dir = parent
	}

	return "."
}
