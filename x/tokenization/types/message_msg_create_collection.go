package types

import (
	sdkerrors "cosmossdk.io/errors"
	sdk "github.com/cosmos/cosmos-sdk/types"
)

const TypeMsgCreateCollection = "msg_create_collection"

var _ sdk.Msg = &MsgCreateCollection{}

func NewMsgCreateCollection(creator string) *MsgCreateCollection {
	return &MsgCreateCollection{
		Creator: creator,
	}
}

func (msg *MsgCreateCollection) Route() string {
	return RouterKey
}

func (msg *MsgCreateCollection) Type() string {
	return TypeMsgCreateCollection
}

func (msg *MsgCreateCollection) GetSigners() []sdk.AccAddress {
	// MustAccAddressFromBech32 panics if address is invalid, which is expected
	// since ValidateBasic() should have already validated the address
	creator := sdk.MustAccAddressFromBech32(msg.Creator)
	return []sdk.AccAddress{creator}
}

func (msg *MsgCreateCollection) GetSignBytes() []byte {
	bz := AminoCdc.MustMarshalJSON(msg)
	return sdk.MustSortJSON(bz)
}

func (msg *MsgCreateCollection) ValidateBasic() error {
	if err := ValidateAddress(msg.Creator, false); err != nil {
		return sdkerrors.Wrapf(ErrInvalidAddress, "invalid creator address (%s)", err)
	}

	// Validate invariants if present
	if msg.Invariants != nil {
		// Validate NoCustomOwnershipTimes with collection approvals
		if msg.Invariants.NoCustomOwnershipTimes {
			// Create a temporary collection to validate invariants
			// Convert InvariantsAddObject to CollectionInvariants (CosmosCoinBackedPath address is generated by keeper, so omitted here)
			tempInvariants := &CollectionInvariants{
				NoCustomOwnershipTimes:      msg.Invariants.NoCustomOwnershipTimes,
				MaxSupplyPerId:              msg.Invariants.MaxSupplyPerId,
				NoForcefulPostMintTransfers: msg.Invariants.NoForcefulPostMintTransfers,
				DisablePoolCreation:         msg.Invariants.DisablePoolCreation,
				EvmQueryChallenges:          msg.Invariants.EvmQueryChallenges,
			}
			tempCollection := &TokenCollection{
				Invariants: tempInvariants,
			}
			if err := ValidateCollectionApprovalsWithInvariants(sdk.Context{}, msg.CollectionApprovals, false, tempCollection); err != nil {
				return err
			}
		}

		// Validate EVM query challenges in invariants
		if err := ValidateEVMQueryChallenges(msg.Invariants.EvmQueryChallenges); err != nil {
			return err
		}
	}

	return nil
}
